package by.ustsinovich.taskmanagementsystem.enums;

/**
 * Enum for task statuses.
 */
public enum TaskStatus {

    /**
     * Open task status.
     */
    OPEN,

    /**
     * In progress task status.
     */
    IN_PROGRESS,

    /**
     * Done task status.
     */
    DONE,

    /**
     * Reopened task status.
     */
    REOPENED,

    /**
     * Closed task status.
     */
    CLOSED

}